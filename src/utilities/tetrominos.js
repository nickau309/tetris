export function randomTetrominoBag() {
  const tetrominos = ["I", "J", "L", "O", "S", "T", "Z"];
  for (let i = tetrominos.length - 1; i > 0; --i) {
    const j = Math.floor(Math.random() * (i + 1));
    [tetrominos[i], tetrominos[j]] = [tetrominos[j], tetrominos[i]];
  }
  return tetrominos;
}
